package fileoperations;

public class Lesson {
    private String name;
    private String term;
    private int grade;
    
    Lesson(){}

    public String getName() {
        return name;
    }

    public String getTerm() {
        return term;
    }

    public int getGrade() {
        return grade;
    }

    public void setName(String name) {
        this.name = name;
    }

    public void setTerm(String term) {
        this.term = term;
    }

    public void setGrade(int grade) {
        this.grade = grade;
    }
    
}
